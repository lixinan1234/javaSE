public class sjlx {
    public static void main(String[] args){
        //byte
        byte a = 10;
        System.out.println(a);
        //short
        short b = 20;
        System.out.println(b);
        //int
        int c = 30;
        System.out.println(c);
        //long
        long d = 4000000000L;
        System.out.println(d);

        //float
        //数据值也需要加一个F作为后缀
        float f = 10.1F;
        System.out.println(f);

        //double
        double s = 20.2;
        System.out.println(s);


        //char
        char h = '我';
        System.out.println(h);


        //boolean
        boolean l = true;
        System.out.println(l);
    }
}
