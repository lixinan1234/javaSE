public class IncreasesDecreases {
    public static void main(String[] args) {
        int result = 10;
        //打印的值加1
        result++;
        System.out.println(result);
        //打印的值加1
        result++;
        System.out.println(result);
        //打印的值减1
        result--;
        System.out.println(result);

    }
}
